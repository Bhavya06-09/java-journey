public class Egswitch 
{
    public static void main(String[] args) 
{
        int month = 2;
        switch(month)
{
            case 1:
                System.out.println("August");
                break;
            case 2:
                System.out.println("September");
                break;
            case 3:
                System.out.println("October");
                break;
        }
    }
}